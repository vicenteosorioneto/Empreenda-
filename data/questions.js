// Função para obter perguntas aleatórias
export const getRandomQuestions = (count = 5) => {
  const shuffled = [...questions].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Função para obter perguntas por categoria
export const getQuestionsByCategory = (category, count = 3) => {
  const filtered = questions.filter(q => q.category === category);
  const shuffled = [...filtered].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Função para obter perguntas por dificuldade
export const getQuestionsByDifficulty = (difficulty, count = 5) => {
  const filtered = questions.filter(q => q.difficulty === difficulty);
  const shuffled = [...filtered].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

